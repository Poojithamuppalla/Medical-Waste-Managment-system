     public void populateTable(){
        DefaultTableModel model = (DefaultTableModel)workRequestJTable.getModel();
        
        model.setRowCount(0);
        WasteCollectorWorkRequest request = new WasteCollectorWorkRequest();
        for(int i=0; i<wasteOrganization.getWorkQueue().getWorkRequestList().size();i++){
            request = (WasteCollectorWorkRequest) wasteOrganization.getWorkQueue().getWorkRequestList().get(i);
            Object[] row = new Object[5];
            row[0] = request.getRequestDate();
            row[1] = request.getSender().getEmployee().getName();
            row[2] = request.getReceiver() == null ? null : request.getReceiver().getEmployee().getName();
            row[3] = request;
            row[4] = request.getDecomposingQuantity();
            model.addRow(row);
        }
    }
    
    private void assignJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_assignJButtonActionPerformed
        int selectedRow = workRequestJTable.getSelectedRow();
        if (selectedRow < 0){
            return;
        }
        WorkRequest request = (WorkRequest)workRequestJTable.getValueAt(selectedRow, 3);
        if("Completed".equalsIgnoreCase(request.getStatus())){
            JOptionPane.showMessageDialog(null, "Request is already completed");
            return;
        }
        if("Pending".equalsIgnoreCase(request.getStatus())||"Processing".equalsIgnoreCase(request.getStatus())){
            JOptionPane.showMessageDialog(null, "Request is already assigned. Please Process the request");
            return;
        }
        request.setReceiver(userAccount);
        request.setStatus("Pending");
        populateTable();
    }//GEN-LAST:event_assignJButtonActionPerformed

    private void processJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_processJButtonActionPerformed
        
        int selectedRow = workRequestJTable.getSelectedRow();
        double totalQuantity = 0;
        if (selectedRow < 0){
            return;
        }
        WasteCollectorWorkRequest request = (WasteCollectorWorkRequest)workRequestJTable.getValueAt(selectedRow, 3);
        if("Sent".equalsIgnoreCase(request.getStatus())){
            JOptionPane.showMessageDialog(null, "Request is not assigned. Please assign the request");
            return;
        }
        if("Completed".equalsIgnoreCase(request.getStatus())){
            JOptionPane.showMessageDialog(null, "Request is already completed");
            return;
        }
        for (int i=0; i<result.size();i++) {
            SegregationTestWorkResult segregationResult = (SegregationTestWorkResult) result.get(i);
            totalQuantity = segregationResult.getDecomposableQuantity();
            totalQuantity = totalQuantity-request.getDecomposingQuantity();
            segregationResult.setDecomposableQuantity(totalQuantity);
        }
        request.setStatus("Completed");
        JOptionPane.showMessageDialog(null, "Request processed succesfully");
        populateTable();
         
    }//GEN-LAST:event_processJButtonActionPerformed

